#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	size;

	if (s)
	{
		i = 0;
		size = ft_strlen(s);
		while (i < size)
		{
			s[i] = '\0';
			i++;
		}
	}
}
